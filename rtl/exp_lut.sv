// =============================================================================
// exp_lut.sv  (VPU primitive — base-2 exponential)
// Computes  e = 2^(-v)  for a non-negative fixed-point magnitude v (Q5.8).
//   v = K.f :  K = integer part (v[12:8], 0..31),  f = fraction (v[7:0]).
//   2^-v = 2^-K · 2^-f = (2^-f) >> K.
//   2^-f for f∈[0,1) is in (0.5,1] -> a 256-entry ROM (Q1.15, value = raw/2^15;
//   1.0 = 0x8000). LUT[i] = round(2^(-i/256)·2^15). Then arithmetic >> K; for
//   K≥16 the result shifts to 0 (true 2^-v ≈ 0). Pure combinational.
//
//   For softmax the VPU feeds v = (max − x)·scale (≥0), so e = 2^(scaled (x−max)).
//   The score scaling lives upstream in the VPU; this is a clean 2^(-v) primitive.
//
//   Approximate (LUT, no interpolation): exponent quantization ≈ ln2·2^-9 ≈ 0.14%.
//   Verified bit-exact vs a Python model of THIS exact recipe + error vs float.
// =============================================================================

